/**
 * PDF Report Generator
 * Generates professional PDF reports for maintenance data
 * Uses PDFKit for PDF generation
 */

import type { GeneratedReport } from './reporting-engine.js';

export interface PDFGeneratorOptions {
  includeLogo?: boolean;
  includeCharts?: boolean;
  includeDetails?: boolean;
  watermark?: string;
}

export class PDFGenerator {
  private logger: any;

  constructor(logger?: any) {
    this.logger = logger || console;
  }

  /**
   * Generate PDF from report data
   */
  async generatePDF(
    report: GeneratedReport,
    options: PDFGeneratorOptions = {}
  ): Promise<Buffer> {
    this.logger.info('Generating PDF report:', report.reportId);

    try {
      // @ts-ignore - Dynamic import
      const PDFDocument = (await import('pdfkit')).default;
      
      return new Promise((resolve, reject) => {
        const chunks: Buffer[] = [];
        const doc = new PDFDocument({ 
          size: 'A4', 
          margin: 50,
          info: {
            Title: report.config.title,
            Author: 'Aditi Sentinel Maintenance System',
            Subject: `${report.config.reportType} Report`,
            Creator: 'Aditi Sentinel',
          },
        });

        // Capture PDF output
        doc.on('data', (chunk: Buffer) => chunks.push(chunk));
        doc.on('end', () => resolve(Buffer.concat(chunks)));
        doc.on('error', reject);

        // Generate PDF content
        this.addHeader(doc, report, options);
        this.addCoverPage(doc, report);
        this.addSummary(doc, report);
        this.addContent(doc, report);

        // Finalize PDF
        doc.end();
      });
    } catch (error) {
      this.logger.error('Failed to generate PDF:', error);
      throw error;
    }
  }

  /**
   * Add header to PDF
   */
  private addHeader(doc: any, report: GeneratedReport, options: PDFGeneratorOptions) {
    doc.fontSize(20)
       .fillColor('#2563eb')
       .text(report.config.title, { align: 'center' });
    
    doc.fontSize(10)
       .fillColor('#666')
       .text(`Report ID: ${report.reportId}`, { align: 'center' })
       .text(`Generated: ${report.config.generatedAt.toLocaleString()}`, { align: 'center' })
       .moveDown(2);
  }

  /**
   * Add cover page
   */
  private addCoverPage(doc: any, report: GeneratedReport) {
    doc.fontSize(12)
       .fillColor('#333')
       .text(`Period: ${report.config.periodStart.toLocaleDateString()} - ${report.config.periodEnd.toLocaleDateString()}`)
       .text(`Generated By: ${report.config.generatedBy}`)
       .moveDown();
  }

  /**
   * Add summary section
   */
  private addSummary(doc: any, report: GeneratedReport) {
    doc.fontSize(16)
       .fillColor('#2563eb')
       .text('Executive Summary', { underline: true })
       .moveDown(0.5);

    doc.fontSize(11)
       .fillColor('#333')
       .text(report.summary)
       .moveDown(1.5);

    // Key metrics
    doc.fontSize(14)
       .fillColor('#2563eb')
       .text('Key Metrics')
       .moveDown(0.5);

    Object.entries(report.metrics).forEach(([key, value]) => {
      doc.fontSize(10)
         .fillColor('#666')
         .text(`${this.formatLabel(key)}: `, { continued: true })
         .fillColor('#333')
         .text(this.formatValue(value));
    });

    doc.moveDown(2);
  }

  /**
   * Add content section
   */
  private addContent(doc: any, report: GeneratedReport) {
    const content = this.formatContent(report);

    content.forEach((section: any) => {
      // Check if we need a new page
      if (doc.y > 650) {
        doc.addPage();
      }

      doc.fontSize(14)
         .fillColor('#2563eb')
         .text(section.title, { underline: true })
         .moveDown(0.5);

      if (section.content) {
        section.content.forEach((line: string) => {
          doc.fontSize(10)
             .fillColor('#333')
             .text(line);
        });
      }

      if (section.table) {
        this.addTable(doc, section.table);
      }

      doc.moveDown(1.5);
    });
  }

  /**
   * Add table to PDF
   */
  private addTable(doc: any, table: any) {
    const startX = 50;
    const startY = doc.y;
    const colWidth = (doc.page.width - 100) / table.headers.length;

    // Headers
    doc.fontSize(9)
       .font('Helvetica-Bold')
       .fillColor('#333');

    table.headers.forEach((header: string, i: number) => {
      doc.text(header, startX + i * colWidth, startY, { width: colWidth, lineBreak: false });
    });

    doc.moveDown(0.5);

    // Rows
    doc.font('Helvetica')
       .fontSize(8);

    table.rows.forEach((row: any[]) => {
      const y = doc.y;
      row.forEach((cell: any, i: number) => {
        doc.text(String(cell), startX + i * colWidth, y, { width: colWidth, lineBreak: false });
      });
      doc.moveDown(0.3);
    });
  }

  /**
   * Generate PDF content structure
   */
  private generatePDFContent(
    report: GeneratedReport,
    options: PDFGeneratorOptions
  ): any {
    return {
      metadata: {
        title: report.config.title,
        author: 'Aditi Sentinel Maintenance System',
        subject: `${report.config.reportType} Report`,
        keywords: 'maintenance, report, CCTV',
        creator: 'Aditi Sentinel',
        producer: 'Aditi Sentinel Reporting Engine',
      },
      
      // Header
      header: {
        logo: options.includeLogo ? 'path/to/logo.png' : null,
        title: report.config.title,
        subtitle: report.summary,
        generatedDate: report.config.generatedAt.toLocaleDateString(),
        reportId: report.reportId,
      },

      // Cover page
      cover: {
        title: report.config.title,
        period: `${report.config.periodStart.toLocaleDateString()} - ${report.config.periodEnd.toLocaleDateString()}`,
        generatedBy: report.config.generatedBy,
        generatedAt: report.config.generatedAt.toLocaleString(),
      },

      // Executive summary
      summary: {
        text: report.summary,
        keyMetrics: this.formatKeyMetrics(report.metrics),
      },

      // Main content
      content: this.formatContent(report),

      // Charts (if enabled)
      charts: options.includeCharts ? this.generateCharts(report) : null,

      // Detailed tables (if enabled)
      details: options.includeDetails ? this.formatDetails(report) : null,

      // Footer
      footer: {
        pageNumbers: true,
        text: 'Confidential - For Internal Use Only',
        watermark: options.watermark,
      },
    };
  }

  /**
   * Format key metrics for display
   */
  private formatKeyMetrics(metrics: Record<string, any>): any[] {
    return Object.entries(metrics).map(([key, value]) => ({
      label: this.formatLabel(key),
      value: this.formatValue(value),
    }));
  }

  /**
   * Format report content
   */
  private formatContent(report: GeneratedReport): any[] {
    const sections = [];

    switch (report.config.reportType) {
      case 'preventive-maintenance':
        sections.push(
          {
            title: 'Preventive Maintenance Overview',
            content: [
              `Total Scheduled Visits: ${report.metrics.scheduled}`,
              `Completed Visits: ${report.metrics.completed}`,
              `Overdue Visits: ${report.metrics.overdue}`,
              `Compliance Rate: ${report.metrics.complianceRate?.toFixed(1)}%`,
            ],
          },
          {
            title: 'Visit Details',
            table: this.formatVisitsTable(report.data.visits),
          }
        );
        break;

      case 'corrective-maintenance':
        sections.push(
          {
            title: 'Corrective Maintenance Summary',
            content: [
              `Total Work Orders: ${report.metrics.total}`,
              `Closed: ${report.metrics.closed}`,
              `Open: ${report.metrics.open}`,
              `Avg Resolution Time: ${report.metrics.avgResolutionHours?.toFixed(1)} hours`,
              `Total Cost: $${report.metrics.totalCost?.toFixed(2)}`,
            ],
          },
          {
            title: 'Work Orders by Severity',
            table: this.formatSeverityTable(report.data.bySeverity),
          }
        );
        break;

      case 'cost-analysis':
        sections.push(
          {
            title: 'Cost Analysis',
            content: [
              `Corrective Maintenance: $${report.metrics.correctiveCost?.toFixed(2)}`,
              `AMC Contracts: $${report.metrics.amcCost?.toFixed(2)}`,
              `Total Cost: $${report.metrics.totalCost?.toFixed(2)}`,
              `Cost Per Work Order: $${report.metrics.costPerWorkOrder?.toFixed(2)}`,
            ],
          },
          {
            title: 'Cost by Category',
            table: this.formatCostByCategoryTable(report.data.costByCategory),
          },
          {
            title: 'Cost Trend',
            table: this.formatCostTrendTable(report.data.costTrend),
          }
        );
        break;

      // Add more report types as needed
      default:
        sections.push({
          title: 'Report Data',
          content: ['Report data is available in JSON format.'],
        });
    }

    return sections;
  }

  /**
   * Generate chart data
   */
  private generateCharts(report: GeneratedReport): any[] {
    // In production, generate actual chart images using chart libraries
    return [
      {
        type: 'bar',
        title: 'Monthly Trend',
        data: report.data.costTrend || [],
      },
      {
        type: 'pie',
        title: 'Distribution',
        data: report.data.bySeverity || {},
      },
    ];
  }

  /**
   * Format detailed tables
   */
  private formatDetails(report: GeneratedReport): any[] {
    return [];
  }

  // ============================================================================
  // Table Formatting
  // ============================================================================

  private formatVisitsTable(visits: any[]): any {
    return {
      headers: ['Date', 'Status', 'Branch', 'Technician'],
      rows: visits.map(v => [
        new Date(v.dueAt).toLocaleDateString(),
        v.status,
        v.branchNodeId || 'N/A',
        v.assignedTo || 'Unassigned',
      ]),
    };
  }

  private formatSeverityTable(bySeverity: any): any {
    return {
      headers: ['Severity', 'Count'],
      rows: Object.entries(bySeverity).map(([severity, count]) => [
        severity.charAt(0).toUpperCase() + severity.slice(1),
        count,
      ]),
    };
  }

  private formatCostByCategoryTable(costByCategory: any): any {
    return {
      headers: ['Category', 'Cost'],
      rows: Object.entries(costByCategory).map(([category, cost]) => [
        category.charAt(0).toUpperCase() + category.slice(1),
        `$${(cost as number).toFixed(2)}`,
      ]),
    };
  }

  private formatCostTrendTable(costTrend: any[]): any {
    return {
      headers: ['Month', 'Cost', 'Work Orders'],
      rows: costTrend.map(t => [
        t.month,
        `$${t.cost.toFixed(2)}`,
        t.workOrders,
      ]),
    };
  }

  // ============================================================================
  // Formatting Helpers
  // ============================================================================

  private formatLabel(key: string): string {
    return key
      .replace(/([A-Z])/g, ' $1')
      .replace(/^./, str => str.toUpperCase())
      .trim();
  }

  private formatValue(value: any): string {
    if (typeof value === 'number') {
      if (Number.isInteger(value)) {
        return value.toString();
      }
      return value.toFixed(2);
    }
    return String(value);
  }
}

